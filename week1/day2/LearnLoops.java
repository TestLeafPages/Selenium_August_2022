package week1.day2;

public class LearnLoops {
public static void main(String[] args) {
	for(int count = 1; count <= 10; count++) {
		System.out.println(count);
	}
	int i = 1;
	while(i <= 10) {
		System.out.println(i);
		i++;
	}
	int j = 1;
	do {
		System.out.println(j);
		j++;
	}while(j <= 10);
	
}
}
